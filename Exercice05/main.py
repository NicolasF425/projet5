def sum(a, b):
    """ Calculate the sum of of a and b.

    Args:
        a (int or float): a number
        b (int or float): a number

    Returns:
        int or float: the sum of a and b.
    """
    return a + b


def subtraction(a, b):
    """ Return the substract of b from a.

    Args:
        a (int orfloat): a number
        b (int or float): a number

    Returns:
        int or float: the result of a - b.
    """
    return a - b
